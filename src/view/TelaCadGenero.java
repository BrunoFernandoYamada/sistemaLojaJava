    private void limparCampos() {

        txtNome.setText(null);
    }

    private boolean verificarCampos() {

        if (txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "\"Preencha os campos Obrigatórios!\"");
            return false;
        } else {
            return true;
        }

    }

    private void carregarTabela(List<Genero> generoLista) {

        String[] colunaTabela = new String[]{"Código do gênero", "Nome do gênero"};

        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunaTabela) {
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };

        tbGeneros.setModel(modeloTabela);

        for (Genero genero : generoLista) {

            modeloTabela.addRow(new String[]{genero.getCodGenero().toString(), genero.getNome()});
        }
    }

    public void atuaizarTB() {
        GeneroDAO generoDAO = new GeneroDAO();
        List<Genero> listaGenero = generoDAO.listar();
        carregarTabela(listaGenero);
    }
