    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        int linha = tbGeneros.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um Gênero na tabela para alterar");
        } else if (verificarCampos()) {
            Integer cod = Integer.parseInt(tbGeneros.getModel().getValueAt(linha, 0).toString());

            GeneroDAO generoDAO = new GeneroDAO();
            Genero genero = new Genero();
            genero.setCodGenero(cod);
            genero.setNome(txtNome.getText());
            generoDAO.editar(genero);

            JOptionPane.showMessageDialog(null, "Cadastro alterado com sucesso!");

        }
        this.atuaizarTB();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (verificarCampos()) {

            Genero genero = new Genero();

            genero.setNome(txtNome.getText());

            GeneroDAO generoDAO = new GeneroDAO();
            generoDAO.inserir(genero);

            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");
        }
        this.atuaizarTB();

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        limparCampos();
    }//GEN-LAST:event_btnLimparActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int linha = tbGeneros.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um Gênero na tabela para excluir");
        } else if (verificarCampos()) {
            Integer cod = Integer.parseInt(tbGeneros.getModel().getValueAt(linha, 0).toString());

            GeneroDAO generoDAO = new GeneroDAO();
            Genero genero = generoDAO.buscar(cod);
            generoDAO.excluir(genero);
            JOptionPane.showMessageDialog(null, "Gênero removido com sucesso!");
        }
        this.atuaizarTB();
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        GeneroDAO generoDAO = new GeneroDAO();
        String palavra = txtConsultar.getText();
        List<Genero> listaGenero = generoDAO.listar(palavra);

        carregarTabela(listaGenero);
    }//GEN-LAST:event_btnConsultarActionPerformed

